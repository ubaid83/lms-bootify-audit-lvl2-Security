function openNav1() {
	if(document.getElementById("mySidenav1").style.width == "2%"){
	document.getElementById("mySidenav1").style.width = "22%";
	document.getElementById("main1").style.marginRight = "22%";
	$('.rightpanel_content').css('height', '');
	$('.rightpanel_content').css('overflow-x', 'hidden');
	$('.rightpanel_content').css('padding-left', '0');
	}
	else{
		document.getElementById("mySidenav1").style.width = "2%";
		document.getElementById("main1").style.marginRight = "2%";
		$('.rightpanel_content').css('height', $(window).height());
		$('.rightpanel_content').css('overflow-y', 'hidden');
		$('.rightpanel_content').css('padding-left', '70px');
	}
}




